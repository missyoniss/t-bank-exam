def fibonacci_in_range():
    try:
        # 1. Запрос ввода у пользователя
        # Ожидается ввод двух чисел через пробел, как в примере
        line = input("Введите начало и конец диапазона через пробел: ")
        start, end = map(int, line.split())

        # Проверка условия: начало должно быть меньше или равно концу
        if start > end:
            print("Ошибка: начало диапазона должно быть меньше конца.")
            return

        # 2. Инициализация первых чисел Фибоначчи
        a, b = 0, 1
        results = []

        # 3. Цикл для генерации чисел Фибоначчи
        # Генерируем числа, пока текущее число 'a' не превысит конец диапазона
        while a <= end:
            # Проверяем, входит ли число в заданный диапазон
            if a >= start:
                results.append(a)
            
            # Переход к следующему числу Фибоначчи
            a, b = b, a + b

        # 4. Вывод результата
        if results:
            # Выводим список найденных чисел через пробел
            print(*(results))
        else:
            # Если список пуст
            print("В заданном диапазоне нет чисел Фибоначчи")

    except ValueError:
        print("Ошибка: пожалуйста, введите два целых числа.")

if __name__ == "__main__":
    fibonacci_in_range()

